//metodos utilizados em strings


let str = 'Matheus';

console.log(str.toUpperCase()); // transforma a string em maiuscula
console.log(str.toLowerCase()); // transforma a string em minuscula

console.log(str.charAt(5)) // seleciona o caractere 
console.log(str[5]) // outra forma de selecionar o caractere 


let frase = 'O rato roeu a roupa do rei de roma'

console.log(frase.slice(2,6)) //O método slice() retorna uma cópia de parte de um array a partir de um subarray criado entre as posições início e fim (fim não é necessário)

console.log(frase.split('')) //O método split() divide uma String em uma lista ordenada de substrings, coloca essas substrings em um array e retorna o array.





